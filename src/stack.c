/* ================================================================================================================== */

/* ================================================================================================================== */

static int stack[sf_stack_STACK_SIZE], ptr = 0;

/* ================================================================================================================== */

void stack_push (int val)
{
  if (ptr < sf_stack_STACK_SIZE)
  {
    stack[ptr++] = val;
  }
}

/* ------------------------------------------------------------------------------------------------------------------ */

int stack_pull (void)
{
  int val = 0;

  if (ptr > 0)
  {
    val = stack[--ptr];
  }

  return (val);
}

/* ------------------------------------------------------------------------------------------------------------------ */

int stack_pop (void)
{
  int val = 0;

  if (ptr > 0)
  {
    val = stack[ptr-1];
  }

  return (val);
}
